<?php

//OPERADORES ARITMÉTICOS

$a = 10;

$b = 2;


echo "Operador artimético de ADIÇÃO '+' <br/>";
echo $a + $b; //operador artimético de ADIÇÃO
echo "<br/>";


echo "Operador artimético de SUBTRAÇÃO '-' <br/>";
echo $a - $b; //operador artimético de SUBTRAÇÃO
echo "<br/>";


echo "Operador artimético de MULTIPLICAÇÃO '*' <br/>";
echo $a * $b; //operador artimético de MULTIPLICAÇÃO
echo "<br/>";


echo "Operador artimético de DIVISÃO '/' <br/>";
echo $a / $b; //operador artimético de DIVISÃO
echo "<br/>";

echo "Operador artimético para pegar o RESTO DA DIVISÃO, ou MÓDULO '%'  <br/>";
echo $a % $b; //operador artimético para pegar o RESTO DA DIVISÃO, ou MÓDULO
echo "<br/>";


echo "Operador artimético de exponenciação '**' <br/>";
echo $a ** $b; //operador artimético de exponenciação
echo "<br/>";

?>