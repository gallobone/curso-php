<?php

//OPERADORES INCREMENTAIS 

//OPERADORES DECREMENTAIS

///////////////////////////////////////////

//Operador de INCREMENTO ++

$a = 10;


//"++" soma mais 1 ao valor que já existe, portanto, neste exemplo, $a passará a valer 11
echo $a++;

echo "<br/>";

echo $a;

echo "<br/>";


//Inserindo o incremento antes da variável, ovalor de +1 é adicionado antes.
echo ++$a;

echo "<br/>";

echo $a;

echo "<br/>";


/////////////////////////////////
//Operador de DECREMENTO

//Subtraindo 1 do valor que já existe na variável.
echo --$a;

?>